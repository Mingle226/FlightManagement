bool deleteFlight(QString id, QString cityFrom, QString cityTo, int startHour, int startMin, int endHour, int endMin)
{
    int city1=getId(cityFrom);
    int city2=getId(cityTo);
    Graph->num_edge--;
    if(Graph->G[city1].FirstEdge!=nullptr)
    {
        PtrToAdjVNode q=Graph->G[city1].FirstEdge;
        PtrToAdjVNode p=Graph->G[city1].FirstEdge;
        if(p->pflight->id.compare(id)==0)
            if(p->pflight->cityTo==city2)
                if(p->pflight->startHour==startHour)
                    if(p->pflight->startMin==startMin)
                        if(p->pflight->endHour==endHour)
                            if(p->pflight->endMin==endMin)
                            {
                                Graph->G[city1].FirstEdge=nullptr;
                                return true;
                            }
        while(p->Next!=nullptr)
        {
            q=p;
            p=p->Next;
            if(p->pflight->id.compare(id)==0)
                if(p->pflight->cityTo==city2)
                    if(p->pflight->startHour==startHour)
                        if(p->pflight->startMin==startMin)
                            if(p->pflight->endHour==endHour)
                                if(p->pflight->endMin==endMin)
                                {
                                    q->Next=p->Next;
                                    delete p;
                                    return true;
                                }

        }
    }
    qDebug()<<"NULLPTR";
    return false;
}

